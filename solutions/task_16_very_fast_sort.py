"""
Задача 16: Очень быстрая сортировка

Дана рекуррентная последовательность:
  A[1] = K
  A[i+1] = (A[i] * M) mod (2^32 - 1) mod L

Отсортировать по неубыванию и найти сумму элементов
на нечётных позициях (1-й, 3-й, 5-й, ...) по модулю L.

Алгоритм:
  1. Генерируем массив по формуле рекуррентности.
  2. Сортируем (Timsort, O(n log n)).
  3. Суммируем элементы с чётными индексами (0, 2, 4, ...)
     — это нечётные позиции в 1-индексации.

Формат входных данных:
  Четыре числа: N, K, M, L.

Формат выходных данных:
  Сумма нечётных по порядку элементов по модулю L.
"""


def fast_sort_sum(n, k, m, l):
    # --- Шаг 1: генерация последовательности ---
    a = [0] * n
    a[0] = k
    MOD = 0xFFFFFFFF  # 2^32 - 1

    for i in range(n - 1):
        # Каждый следующий элемент вычисляется по формуле
        a[i + 1] = ((a[i] * m) % MOD) % l

    # --- Шаг 2: сортировка ---
    a.sort()

    # --- Шаг 3: суммирование нечётных по порядку ---
    # Нечётные позиции (1, 3, 5, ...) = индексы 0, 2, 4, ...
    res = 0
    for i in range(0, n, 2):
        res = (res + a[i]) % l

    return res


if __name__ == '__main__':
    # Пример 1
    print(fast_sort_sum(3, 1, 2, 10))

    # Пример 2: один элемент
    print(fast_sort_sum(1, 5, 3, 100))  # 5
