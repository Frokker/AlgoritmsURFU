# Задача 09: Вырубка деревьев
#
# Король Флатландии решил вырубить некоторые деревья,
# растущие перед его дворцом. Деревья перед дворцом короля
# посажены в ряд, всего там растет n деревьев, расстояния
# между соседними деревьями одинаковы. После вырубки перед
# дворцом должно остаться m деревьев, и расстояния между
# соседними деревьями должны быть одинаковыми.
# Требуется написать программу, которая по заданным числам
# n и m определит, сколько существует способов вырубки
# некоторых из n деревьев.
#
# Формат входных данных:
# Два целых числа n и m.
#
# Формат выходных данных:
# Количество способов вырубки деревьев.


def tree_cutting(n, m):
    ways = 0

    # Обработка граничных случаев
    if m == 0:
        ways = 1  # Существует только 1 способ — вырубить все деревья
    elif m == 1:
        ways = n  # Существует n способов оставить ровно одно дерево
    else:
        # Перебор возможных шагов (расстояний) между оставленными деревьями
        # Максимальный шаг d возможен, если (m - 1) * d < n
        d = 1
        while (m - 1) * d < n:
            # Для конкретного шага d количество возможных стартовых позиций
            # равно n - (m - 1) * d
            ways += n - (m - 1) * d
            d += 1

    return ways


if __name__ == "__main__":
    # Пример 1: 5 деревьев, оставить 3
    print(tree_cutting(5, 3))  # 4

    # Пример 2: 10 деревьев, оставить 2
    print(tree_cutting(10, 2))  # 45

    # Пример 3: 10 деревьев, оставить 3
    print(tree_cutting(10, 3))  # 20
