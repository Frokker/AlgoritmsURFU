# Задача 09: Вырубка деревьев
#
# Из n деревьев в ряд (с равными промежутками) нужно оставить m деревьев
# так, чтобы расстояния между соседними оставшимися были одинаковы.
# Сколько существует способов?
#
# Ключевое наблюдение:
#   Оставшиеся m деревьев образуют арифметическую прогрессию
#   с шагом d: start, start+d, start+2d, ..., start+(m-1)*d.
#
# Для каждого допустимого шага d считаем, сколько стартовых позиций
# возможно: start может быть от 0 до n-1-(m-1)*d,
# то есть n-(m-1)*d вариантов.
#
# Итого: answer = sum(n - (m-1)*d) для d = 1, 2, ..., floor((n-1)/(m-1))
#
# Формат входных данных:
#   n — количество деревьев, m — сколько оставить.
#
# Формат выходных данных:
#   Количество способов вырубки.
#
# Сложность: O(n/m) по времени.


def tree_cutting(n, m):
    ways = 0

    if m == 0:
        # Вырубить всё — ровно один способ
        ways = 1
    elif m == 1:
        # Оставить любое одно дерево — n способов
        ways = n
    else:
        # Перебираем шаг d между оставшимися деревьями
        d = 1
        while (m - 1) * d < n:
            # Для данного шага d стартовая позиция может быть
            # от 0 до n-1-(m-1)*d, что даёт n-(m-1)*d вариантов
            ways += n - (m - 1) * d
            d += 1

    return ways


if __name__ == "__main__":
    # Пример 1: 5 деревьев, оставить 3
    # d=1: (0,1,2),(1,2,3),(2,3,4) = 3; d=2: (0,2,4) = 1 → итого 4
    print(tree_cutting(5, 3))  # 4

    # Пример 2: 10 деревьев, оставить 2
    print(tree_cutting(10, 2))  # 45

    # Пример 3: 10 деревьев, оставить 3
    print(tree_cutting(10, 3))  # 20
