# Задача 05: Длинная арифметика
#
# На вход подается три строки. Первая содержит представление
# длинного десятичного числа (первый операнд), вторая — представление
# операции (строки "+" или "-"), третья — представление второго операнда.
# Длина первой и третьей строки ограничены 1000 символами.
# Требуется исполнить операцию и вывести результат
# в десятичном представлении.
#
# Формат входных данных:
# Первая строка — первый операнд (длинное десятичное число).
# Вторая строка — операция (+ или -).
# Третья строка — второй операнд (длинное десятичное число).
#
# Формат выходных данных:
# Результат операции в десятичном представлении.


def long_arithmetic(a, op, b):
    # В Python реализована встроенная поддержка длинной арифметики
    result = 0

    if op == '+':
        result = int(a) + int(b)
    elif op == '-':
        result = int(a) - int(b)

    return str(result)


if __name__ == "__main__":
    # Пример 1: сложение
    print(long_arithmetic("123", "+", "456"))  # 579

    # Пример 2: вычитание
    print(long_arithmetic("456", "-", "123"))  # 333

    # Пример 3: большие числа
    print(long_arithmetic("9" * 100, "+", "1"))  # 1 followed by 100 zeros

    # Пример 4: отрицательный результат
    print(long_arithmetic("100", "-", "250"))  # -150
