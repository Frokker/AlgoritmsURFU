# Задача 05: Длинное сложение и вычитание
#
# Даны два числа (до 1000 цифр) и операция (+ или -).
# Вычислить результат.
#
# В большинстве языков (C++, Java) стандартные типы int/long
# вмещают максимум ~19 цифр, поэтому нужна длинная арифметика
# (сложение/вычитание в столбик поразрядно).
#
# В Python int поддерживает числа произвольной длины,
# поэтому решение тривиально.
#
# Формат входных данных:
#   a  — первый операнд (строка до 1000 символов).
#   op — операция ("+" или "-").
#   b  — второй операнд (строка до 1000 символов).
#
# Формат выходных данных:
#   Результат операции в виде строки.


def long_arithmetic(a, op, b):
    # Python автоматически работает с длинными числами —
    # int("9" * 1000) создаст число из 1000 цифр без проблем
    if op == '+':
        result = int(a) + int(b)
    elif op == '-':
        result = int(a) - int(b)

    return str(result)


if __name__ == "__main__":
    # Пример 1: простое сложение
    print(long_arithmetic("123", "+", "456"))  # 579

    # Пример 2: простое вычитание
    print(long_arithmetic("456", "-", "123"))  # 333

    # Пример 3: очень большие числа (100 девяток + 1)
    print(long_arithmetic("9" * 100, "+", "1"))

    # Пример 4: отрицательный результат
    print(long_arithmetic("100", "-", "250"))  # -150
