"""
Задача 15: Выпуклая оболочка (периметр)

Дан набор из N точек на плоскости. Найти периметр наименьшего
выпуклого многоугольника, содержащего все точки.

Представьте, что вы забили гвозди в доску и натянули на них
резинку — форма резинки и есть выпуклая оболочка.

Алгоритм: «Монотонная цепь» Эндрю (Andrew's monotone chain).
  1. Сортируем точки по x (при равенстве — по y).
  2. Строим НИЖНЮЮ часть оболочки (слева → направо):
     добавляем точки, убирая те, что дают правый поворот.
  3. Строим ВЕРХНЮЮ часть (справа → налево) аналогично.
  4. Склеиваем обе части.

Направление поворота определяется векторным произведением:
  cross(O, A, B) = (A-O) x (B-O)
  > 0 — левый поворот (точка снаружи, оставляем)
  <= 0 — правый поворот или прямо (средняя точка внутри, убираем)

Сложность: O(n log n) — из-за сортировки.
"""

import math


def cross_product(o, a, b):
    """Векторное произведение OA x OB.
    Положительное = левый поворот, отрицательное = правый."""
    return (a[0] - o[0]) * (b[1] - o[1]) - (a[1] - o[1]) * (b[0] - o[0])


def convex_hull(points):
    """Строит выпуклую оболочку набора точек.
    Возвращает список вершин оболочки в порядке обхода."""
    points = sorted(points)
    if len(points) <= 1:
        return points

    # Нижняя оболочка: идём слева направо
    lower = []
    for p in points:
        # Убираем точки, дающие правый поворот (cross <= 0)
        while len(lower) >= 2 and cross_product(lower[-2], lower[-1], p) <= 0:
            lower.pop()
        lower.append(p)

    # Верхняя оболочка: идём справа налево
    upper = []
    for p in reversed(points):
        while len(upper) >= 2 and cross_product(upper[-2], upper[-1], p) <= 0:
            upper.pop()
        upper.append(p)

    # Склеиваем, убирая дублирующиеся крайние точки
    return lower[:-1] + upper[:-1]


def distance(p1, p2):
    """Евклидово расстояние между двумя точками."""
    return math.sqrt((p1[0] - p2[0])**2 + (p1[1] - p2[1])**2)


def convex_hull_perimeter(n, points):
    """Вычисляет периметр выпуклой оболочки."""
    if n <= 1:
        return 0.0

    hull = convex_hull(points)

    if len(hull) <= 1:
        return 0.0

    # Суммируем длины всех рёбер оболочки
    perimeter = 0.0
    for i in range(len(hull)):
        perimeter += distance(hull[i], hull[(i + 1) % len(hull)])

    return round(perimeter, 2)


if __name__ == '__main__':
    # Пример: треугольник 3-4-5
    points = [(0, 0), (3, 0), (0, 4)]
    print(convex_hull_perimeter(3, points))  # 12.0

    # Пример: квадрат
    points2 = [(0, 0), (1, 0), (1, 1), (0, 1)]
    print(convex_hull_perimeter(4, points2))  # 4.0
