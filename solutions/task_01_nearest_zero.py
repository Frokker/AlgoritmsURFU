# Задача 01: Ближайший ноль
#
# Улица, на которой хочет жить Тимофей, имеет длину n,
# то есть состоит из n одинаковых идущих подряд участков.
# На каждом участке либо уже построен дом, либо участок пустой.
# Пустые участки обозначены нулями.
# Для каждого участка найти расстояние до ближайшего нуля.
#
# Формат входных данных:
#   n — длина улицы (1 <= n <= 10^6).
#   a — массив из n неотрицательных чисел.
#
# Формат выходных данных:
#   Массив расстояний до ближайшего нуля для каждого участка.
#
# Алгоритм: два прохода (O(n) по времени, O(n) по памяти).
#   Ближайший ноль может быть слева или справа.
#   Проход 1 (→): для каждого элемента запоминаем расстояние до последнего нуля слева.
#   Проход 2 (←): аналогично справа, берём минимум.


def nearest_zero(n, a):
    # Инициализируем расстояния бесконечностью —
    # если нуля слева/справа ещё не было, расстояние неизвестно
    distances = [float('inf')] * n

    # --- Проход 1: слева направо ---
    # Запоминаем позицию последнего встреченного нуля
    zero_pos = -float('inf')
    for i in range(n):
        if a[i] == 0:
            # Нашли ноль — обновляем его позицию, расстояние = 0
            zero_pos = i
            distances[i] = 0
        else:
            # Расстояние до последнего нуля слева: i - zero_pos
            distances[i] = min(distances[i], i - zero_pos)

    # --- Проход 2: справа налево ---
    # Теперь ищем ближайший ноль справа
    zero_pos = float('inf')
    for i in range(n - 1, -1, -1):
        if a[i] == 0:
            zero_pos = i
            distances[i] = 0
        else:
            # Берём минимум между расстоянием слева и справа
            distances[i] = min(distances[i], zero_pos - i)

    return distances


if __name__ == "__main__":
    # Пример 1: [0, 1, 4, 9, 0] -> [0, 1, 2, 1, 0]
    print(nearest_zero(5, [0, 1, 4, 9, 0]))

    # Пример 2: ноль в середине
    print(nearest_zero(7, [5, 4, 3, 0, 1, 2, 3]))

    # Пример 3: несколько нулей
    print(nearest_zero(9, [1, 2, 0, 3, 4, 5, 0, 1, 2]))
