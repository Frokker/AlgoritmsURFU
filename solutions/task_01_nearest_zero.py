# Задача 01: Ближайший ноль
#
# Улица, на которой хочет жить Тимофей, имеет длину n,
# то есть состоит из n одинаковых идущих подряд участков.
# На каждом участке либо уже построен дом, либо участок пустой.
# Чтобы оптимально выбрать место для строительства,
# Тимофей хочет для каждого участка знать расстояние
# до ближайшего пустого участка.
# Пустые участки обозначены нулями.
#
# Формат входных данных:
# В первой строке дана длина улицы n (1 <= n <= 10^6).
# В следующей строке записаны n целых неотрицательных чисел.
#
# Формат выходных данных:
# Для каждого из участков выведите расстояние до ближайшего нуля.


def nearest_zero(n, a):
    # Инициализация массива расстояний бесконечностью
    distances = [float('inf')] * n

    # Первый проход: слева направо
    zero_pos = -float('inf')
    for i in range(n):
        if a[i] == 0:
            zero_pos = i
            distances[i] = 0
        else:
            # Расстояние до последнего встреченного нуля слева
            distances[i] = min(distances[i], i - zero_pos)

    # Второй проход: справа налево
    zero_pos = float('inf')
    for i in range(n - 1, -1, -1):
        if a[i] == 0:
            zero_pos = i
            distances[i] = 0
        else:
            # Минимум между нулем слева и нулем справа
            distances[i] = min(distances[i], zero_pos - i)

    return distances


if __name__ == "__main__":
    # Пример 1: [0, 1, 4, 9, 0] -> [0, 1, 2, 1, 0]
    print(nearest_zero(5, [0, 1, 4, 9, 0]))

    # Пример 2: ноль в середине
    print(nearest_zero(7, [5, 4, 3, 0, 1, 2, 3]))

    # Пример 3: несколько нулей
    print(nearest_zero(9, [1, 2, 0, 3, 4, 5, 0, 1, 2]))
