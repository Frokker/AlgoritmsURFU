"""
Задача 18: Музей (максимум посетителей одновременно)

Даны интервалы посещения музея (время прихода и ухода).
Найти максимальное число посетителей, находившихся
в музее одновременно.

Алгоритм: заметающая прямая (sweep line).
  Каждое посещение разбиваем на два события:
    - приход  (+1) в момент t1
    - уход    (-1) в момент t2
  Сортируем события по времени.
  При совпадении времени: приход (тип=0) обрабатывается
  РАНЬШЕ ухода (тип=1), чтобы учесть одновременное присутствие.

  Проходим по событиям, ведём текущий счётчик.
  Максимум счётчика = ответ.

Сложность: O(n log n) — из-за сортировки событий.
"""


def museum_max_visitors(n, visits):
    # --- Шаг 1: создаём список событий ---
    events = []
    for arrive_str, depart_str in visits:
        # Преобразуем "ЧЧ:ММ" в минуты от полуночи
        ah, am = map(int, arrive_str.split(':'))
        dh, dm = map(int, depart_str.split(':'))
        arrive = ah * 60 + am
        depart = dh * 60 + dm
        # Тип события: 0 = приход (обрабатывается первым), 1 = уход
        events.append((arrive, 0))   # +1 посетитель
        events.append((depart, 1))   # -1 посетитель

    # --- Шаг 2: сортировка ---
    # По времени, а при совпадении — приход (0) раньше ухода (1)
    events.sort()

    # --- Шаг 3: проход по событиям ---
    current = 0
    max_visitors = 0
    for time, event_type in events:
        if event_type == 0:
            current += 1    # кто-то пришёл
        else:
            current -= 1    # кто-то ушёл
        if current > max_visitors:
            max_visitors = current

    return max_visitors


if __name__ == '__main__':
    # Пример 1: частичное пересечение → 2
    visits = [("09:00", "11:00"), ("10:00", "12:00")]
    print(museum_max_visitors(2, visits))

    # Пример 2: без пересечения → 1
    visits = [("09:00", "10:00"), ("11:00", "12:00")]
    print(museum_max_visitors(2, visits))

    # Пример 3: вложенные интервалы → 4
    visits = [
        ("08:00", "18:00"),
        ("09:00", "17:00"),
        ("10:00", "16:00"),
        ("11:00", "15:00"),
    ]
    print(museum_max_visitors(4, visits))
